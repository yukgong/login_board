DROP TABLE BBS
CASCADE CONSTRAINTS; --지우기

DROP SEQUENCE SEQ_BBS; --지우기

CREATE TABLE BBS(
	SEQ NUMBER(8) PRIMARY KEY,
	EMAIL VARCHAR2(50) NOT NULL,
	
	REF NUMBER(8) NOT NULL,
	STEP NUMBER(8) NOT NULL,
	DEPTH NUMBER(8) NOT NULL,
	
	TITLE VARCHAR2(200) NOT NULL,
	CONTENT VARCHAR2(4000), -- 글이 길수도 있어서!
	WDATE DATE NOT NULL,
	DEL NUMBER(1) NOT NULL, --지워졌는지 아닌지만 파악하면 됨
	READCOUNT NUMBER(8) NOT NULL
);

-- 중복되지 않고 계속 넘어가는 번호
CREATE SEQUENCE SEQ_BBS 
START WITH 1
INCREMENT BY 1;

-- MEMBER 테이블과 join
ALTER TABLE BBS
ADD CONSTRAINT FK_BBS_EMAIL FOREIGN KEY(EMAIL)
REFERENCES MEMBER(EMAIL); 

